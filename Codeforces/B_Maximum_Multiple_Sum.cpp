#include <bits/stdc++.h>

using namespace std;
#define endll '\n'

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int t; cin >> t;

    while(t--)
    {
        int n; cin >> n;

        if(n == 3)
        {
            cout << "3" << endll;
        }
        else
        {
            cout << "2" << endll;
        }
    }
    return 0;
}