#include <bits/stdc++.h>

using namespace std;
#define endll '\n'

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int t; cin >> t;

    while(t--)
    {
        string s, b, c;
        cin >> s >> b >> c;
        

        cout << s[0] << b[0] << c[0] << endll;
    }
    return 0;
}